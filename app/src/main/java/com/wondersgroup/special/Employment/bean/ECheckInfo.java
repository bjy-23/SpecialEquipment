package com.wondersgroup.special.employment.bean;

import java.io.Serializable;

/**
 * Created by bjy on 2016/12/21.
 */

public class ECheckInfo implements Serializable{
    //主键
    private String uuid;

    //考核日期
    private String assessDate;

    //考核内容
    private String assessContent;

    //考核机构
    private String assessOrgan;

    //考核成绩
    private String assessScore;

    //闭卷考试成绩
    private String closedbookScore;

    //实际操作成绩
    private String operateScore;

    //开卷考试成绩
    private String openbookScore;

    //人员表id
    private String personId;

    //持证项目
    private String certPro;

    public String getAssessContent() {
        return assessContent;
    }

    public void setAssessContent(String assessContent) {
        this.assessContent = assessContent;
    }

    public String getAssessDate() {
        return assessDate;
    }

    public void setAssessDate(String assessDate) {
        this.assessDate = assessDate;
    }

    public String getAssessOrgan() {
        return assessOrgan;
    }

    public void setAssessOrgan(String assessOrgan) {
        this.assessOrgan = assessOrgan;
    }

    public String getAssessScore() {
        return assessScore;
    }

    public void setAssessScore(String assessScore) {
        this.assessScore = assessScore;
    }

    public String getCertPro() {
        return certPro;
    }

    public void setCertPro(String certPro) {
        this.certPro = certPro;
    }

    public String getClosedbookScore() {
        return closedbookScore;
    }

    public void setClosedbookScore(String closedbookScore) {
        this.closedbookScore = closedbookScore;
    }

    public String getOpenbookScore() {
        return openbookScore;
    }

    public void setOpenbookScore(String openbookScore) {
        this.openbookScore = openbookScore;
    }

    public String getOperateScore() {
        return operateScore;
    }

    public void setOperateScore(String operateScore) {
        this.operateScore = operateScore;
    }

    public String getPersonId() {
        return personId;
    }

    public void setPersonId(String personId) {
        this.personId = personId;
    }

    public String getUuid() {
        return uuid;
    }

    public void setUuid(String uuid) {
        this.uuid = uuid;
    }
}
